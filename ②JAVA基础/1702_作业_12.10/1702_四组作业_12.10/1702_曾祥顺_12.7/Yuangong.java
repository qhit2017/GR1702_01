public class Yuangong {

	private String name;
	private int number;
	public Yuangong(String name, int number) {
		this.name = name;
		this.number = number;
	}
	@Override
	public String toString() {
		return "Yuangong [name=" + name + ", number=" + number + "]";
	}
	
	
	
}
